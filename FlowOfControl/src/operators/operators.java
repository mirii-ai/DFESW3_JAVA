package operators;

public class operators {
	
	//operators are functions you use to change numerical value

	public static int addMethod() {
		int result = 5+5;
		return result;
	}
	
	public static float divideMethod() {
		float result = 10f/3f; //int prints a whole number, not a decimal. Putting float and 'f' prints the decimal answer
		return result;
	}
	
	public static int addNums(int num1, int num2) {
		int result = num1 + num2;
		return result;
	}

}
