package iteration;

public class forLoops {

	
	public static void countToTen() {
		//count to 10 printing out each number in between
		
		for(int i = 0; //initialisation, starting from 0
				i < 10; //condition, do the code while i is less than 10
				i++) { //iterator- adding 1 to i after every loop
			System.out.println(i);
			
		}
				
	}
}
